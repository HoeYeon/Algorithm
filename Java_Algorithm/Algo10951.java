package Java_Algorithm;

import java.util.Scanner;

public class Algo10951 {
    public static void main(String[] args) throws Exception {
        Scanner s = new Scanner(System.in);

        while (s.hasNextInt()) {
            int a = s.nextInt();
            int b = s.nextInt();

            System.out.println(a + b);
        }
    }
}
